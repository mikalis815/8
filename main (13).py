def power_full(x, n):
    """
    power_full для полной версии (с поддержкой отрицательных степеней)
    Аргументы:
    x - основание степени
    n - показатель степени (любое целое число)
    """
    # Базовый случай: степень 0
    if n == 0:
        return 1
    
    # Обработка отрицательных степеней
    if n < 0:
        # x^(-n) = 1 / (x^n)
        return 1 / power_full(x, -n)
    
    # Рекурсивный случай для положительных степеней
    return x * power_full(x, n - 1)
    
    # Примеры:
print(f"2^(-3) = {power_full(2, -3)}")
print(f"5^(-2) = {power_full(5, -2)}")
print(f"(-2)^3 = {power_full(-2, 3)}")
print(f"(-3)^2 = {power_full(-3, 2)}")