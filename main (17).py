def find_paths(x=0, y=0, path=""):
    """
    Находит все пути от (0,0) до (4,4) в лабиринте 5x5.
    Двигаться можно только ВПРАВО (R) и ВНИЗ (D).
    Аргументы:
        x, y: текущие координаты
        path: пройденный путь в виде строки (например, "RRDDR")
    """
    # Если дошли до правого нижнего угла (4,4)
    if x == 4 and y == 4:
        #Найден путь:
        print(path)
        return
    
    # Двигаемся ВПРАВО, если не достигли правой границы
    if x < 4:
        find_paths(x + 1, y, path + "R")
    
    # Двигаемся ВНИЗ, если не достигли нижней границы
    if y < 4:
        find_paths(x + 0, y + 1, path + "D")

# Запускаем поиск из начальной точки (0,0)
#Все пути в лабиринте
find_paths()